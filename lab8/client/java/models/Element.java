package models;

/**
 * Абстрактный базовый класс для элементов, требующих уникального идентификатора.
 * Обеспечивает обязательное наличие метода для получения ID у наследников.
 */
public abstract class Element {
    /**
     * Возвращает уникальный идентификатор элемента.
     * Реализация зависит от конкретного подкласса.
     *
     * @return Уникальный ID элемента (может быть {@code null}, если элемент не инициализирован).
     */
    public abstract Integer getId();
}