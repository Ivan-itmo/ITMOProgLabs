package exceptions;

/**
 * Исключение, выбрасываемое при попытке выполнения операции с пользователем,
 * который не существует в системе.
 *
 * <p>Наследует {@link RuntimeException}, что означает что это неконтролируемое исключение
 * и не требует обязательного объявления в сигнатуре метода.
 *
 * <p>Типичные сценарии использования:
 * <ul>
 *   <li>Попытка аутентификации несуществующего пользователя</li>
 *   <li>Запрос информации о несуществующем пользователе</li>
 *   <li>Попытка изменения данных несуществующего пользователя</li>
 *   <li>Удаление несуществующей учетной записи</li>
 * </ul>
 */
public class NotFoundUserException extends RuntimeException {
    /**
     * Создает новое исключение с указанным сообщением об ошибке.
     *
     * @param message детальное сообщение об ошибке, должно содержать:
     *                - идентификатор или логин не найденного пользователя
     *                - контекст операции, в которой возникла ошибка
     *                Пример: "Пользователь с логином 'admin' не найден"
     */
    public NotFoundUserException(String message) {
        super(message);
    }
}