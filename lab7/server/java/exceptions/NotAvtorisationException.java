package exceptions;

/**
 * Исключение, возникающее при попытке выполнения операции,
 * требующей авторизации, когда пользователь не аутентифицирован.
 *
 * <p>Наследуется от {@link RuntimeException}, что означает,
 * что это неконтролируемое (unchecked) исключение.
 *
 * <p>Типичные случаи возникновения:
 * <ul>
 *   <li>Попытка доступа к защищенным ресурсам без авторизации</li>
 *   <li>Выполнение команд, требующих аутентификации</li>
 *   <li>Неудачная попытка авторизации с неверными учетными данными</li>
 * </ul>
 */
public class NotAvtorisationException extends RuntimeException {
    /**
     * Создает новое исключение с указанным сообщением об ошибке.
     *
     * @param message детальное сообщение об ошибке, описывающее причину исключения
     *                (например: "Требуется авторизация для выполнения этой команды",
     *                "Неверные учетные данные" или "Сессия истекла")
     */
    public NotAvtorisationException(String message) {
        super(message);
    }
}