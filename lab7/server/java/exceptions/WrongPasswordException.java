package exceptions;

/**
 * Исключение, возникающее при попытке аутентификации с неверным паролем.
 * <p>
 * Наследуется от {@link RuntimeException}, что означает, что это неконтролируемое (unchecked) исключение.
 *
 * <p><b>Типичные случаи использования:</b>
 * <ul>
 *   <li>Ввод неправильного пароля при входе в систему</li>
 *   <li>Попытка изменения данных с неверным текущим паролем</li>
 *   <li>Ошибка подтверждения пароля при регистрации или смене пароля</li>
 * </ul>
 *
 * <p><b>Рекомендации по обработке:</b>
 * <ul>
 *   <li>Сообщать пользователю о неверном пароле без излишних деталей</li>
 *   <li>Предоставлять ограниченное количество попыток ввода</li>
 *   <li>Фиксировать попытки неверного ввода для безопасности</li>
 * </ul>
 */
public class WrongPasswordException extends RuntimeException {
    /**
     * Создает новое исключение с указанным сообщением об ошибке.
     *
     * @param message детальное сообщение об ошибке (например: "Неверный пароль" или
     *               "Введенный пароль не соответствует требованиям безопасности").
     *               Рекомендуется избегать сообщений, раскрывающих систему безопасности.
     */
    public WrongPasswordException(String message) {
        super(message);
    }
}