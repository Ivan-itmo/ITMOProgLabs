package exceptions;

/**
 * Исключение, выбрасываемое при попытке выполнения операции,
 * требующей уникального пользователя, когда такой пользователь уже существует.
 *
 * <p>Наследует {@link RuntimeException}, что означает, что это неконтролируемое исключение.
 *
 * <p>Типичные случаи использования:
 * <ul>
 *   <li>Регистрация пользователя с уже существующим логином</li>
 *   <li>Создание дублирующей записи пользователя</li>
 *   <li>Попытка повторного сохранения существующего пользователя</li>
 * </ul>
 */
public class HavingUserException extends RuntimeException {
    /**
     * Создает новое исключение с указанным сообщением об ошибке.
     *
     * @param message детализированное сообщение об ошибке,
     *                которое должно содержать информацию о существующем пользователе
     *                и причине конфликта (например, "Пользователь с логином 'test' уже существует")
     */
    public HavingUserException(String message) {
        super(message);
    }
}